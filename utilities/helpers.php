function product_display_name(array $product): string
{
    $name = trim((string) ($product['name'] ?? ''));
    $sku = strtoupper(trim((string) ($product['sku'] ?? '')));

    $knownIdNames = [
        'ID-LACE-001' => 'ID Lace',
        'ID-CARD-001' => 'ID Card',
        'ID-PRINT-001' => 'ID Printing',
        'PRINT-ID' => 'ID Printing',
        'ID-REPLACE-001' => 'ID Replacement',
        'ID-FEE-001' => 'ID Processing Fee',
    ];
    if (isset($knownIdNames[$sku])) {
        return $knownIdNames[$sku];
    }

    $normalized = strtolower(preg_replace('/[^a-z0-9]+/i', ' ', $name) ?? '');
    $legacyNames = [
        'provide id' => 'ID Processing Fee',
        'sell id' => 'ID Processing Fee',
        'id service' => 'ID Processing Fee',
        'id items' => 'ID Supplies',
    ];

    return $legacyNames[trim($normalized)] ?? $name;
}

