function find_person(PDO $pdo, int $personId, bool $approvedOnly = true): ?array
{
    if ($personId <= 0) {
        return null;
    }

    $sql = 'SELECT id, full_name, person_code, department, role_or_position, contact_info, status
        FROM people
        WHERE id = :id';
    if ($approvedOnly) {
        $sql .= ' AND status = "approved"';
    }

    $stmt = $pdo->prepare($sql);
    $stmt->execute(['id' => $personId]);
    $person = $stmt->fetch();

    return $person ?: null;
}

