function handle_person_post(PDO $pdo, array $user, string $defaultRedirect): bool
{
    $action = (string) ($_POST['action'] ?? '');
    if (!in_array($action, ['add_person', 'update_person'], true)) {
        return false;
    }

    $returnTo = trim((string) ($_POST['return_to'] ?? $defaultRedirect));
    if ($returnTo === '' || str_contains($returnTo, "\n") || str_contains($returnTo, "\r") || preg_match('#^https?://#i', $returnTo)) {
        $returnTo = $defaultRedirect;
    }

    $fullName = trim((string) ($_POST['full_name'] ?? ''));
    $personCode = trim((string) ($_POST['person_code'] ?? ''));
    $department = trim((string) ($_POST['department'] ?? ''));
    $role = trim((string) ($_POST['role_or_position'] ?? ''));
    $contact = trim((string) ($_POST['contact_info'] ?? ''));

    if ($fullName === '') {
        set_flash('error', 'Full name is required.');
        redirect($returnTo);
    }

    if ($action === 'update_person') {
        // Only Admin can update person records
        if ($user['role'] !== 'admin') {
            set_flash('error', 'You do not have permission to perform this action.');
            redirect($returnTo);
        }

        $personId = (int) ($_POST['person_id'] ?? 0);
        $status = (string) ($_POST['status'] ?? 'pending');
        
        if ($personId <= 0 || !in_array($status, ['pending', 'active', 'inactive', 'rejected'], true)) {
            set_flash('error', 'Invalid person update.');
            redirect($returnTo);
        }

        $stmt = $pdo->prepare('SELECT status FROM people WHERE id = :id');
        $stmt->execute(['id' => $personId]);
        $oldPerson = $stmt->fetch();
        if (!$oldPerson) {
            set_flash('error', 'Person not found.');
            redirect($returnTo);
        }
        $oldStatus = $oldPerson['status'];

        $stmt = $pdo->prepare('UPDATE people
            SET full_name = :full_name,
                person_code = :person_code,
                department = :department,
                role_or_position = :role_or_position,
                contact_info = :contact_info,
                status = :status,
                approved_by = CASE WHEN :status = "active" THEN :approved_by ELSE approved_by END,
                approved_at = CASE WHEN :status = "active" THEN COALESCE(approved_at, NOW()) ELSE approved_at END
            WHERE id = :id');
        $stmt->execute([
            'id' => $personId,
            'full_name' => $fullName,
            'person_code' => $personCode !== '' ? $personCode : null,
            'department' => $department !== '' ? $department : null,
            'role_or_position' => $role !== '' ? $role : null,
            'contact_info' => $contact !== '' ? $contact : null,
            'status' => $status,
            'approved_by' => (int) $user['id'],
        ]);

        $auditAction = match($status) {
            'active' => 'person_approved',
            'rejected' => 'person_rejected',
            default => 'person_updated',
        };

        audit_log($pdo, $user, $auditAction, 'people', 'person', $personId, [
            'old_status' => $oldStatus,
            'new_status' => $status,
        ]);

        set_flash('success', 'Person record updated: ' . ucfirst($status) . '.');
        redirect($returnTo);
    }

    // Add new person record
    $isAdmin = $user['role'] === 'admin';
    $status = $isAdmin ? (string) ($_POST['status'] ?? 'active') : 'pending';
    
    if (!in_array($status, ['pending', 'active', 'inactive', 'rejected'], true)) {
        $status = $isAdmin ? 'active' : 'pending';
    }

    $stmt = $pdo->prepare('INSERT INTO people (full_name, person_code, department, role_or_position, contact_info, status, created_by, approved_by, approved_at)
        VALUES (:full_name, :person_code, :department, :role_or_position, :contact_info, :status, :created_by, :approved_by, :approved_at)');
    $stmt->execute([
        'full_name' => $fullName,
        'person_code' => $personCode !== '' ? $personCode : null,
        'department' => $department !== '' ? $department : null,
        'role_or_position' => $role !== '' ? $role : null,
        'contact_info' => $contact !== '' ? $contact : null,
        'status' => $status,
        'created_by' => (int) $user['id'],
        'approved_by' => $status === 'active' ? (int) $user['id'] : null,
        'approved_at' => $status === 'active' ? date('Y-m-d H:i:s') : null,
    ]);
    $personId = (int) $pdo->lastInsertId();
    audit_log($pdo, $user, 'create_person', 'people', 'person', $personId, ['status' => $status]);
    
    if ($status === 'active') {
        set_flash('success', 'Person record saved.');
    } else {
        set_flash('success', 'Person request submitted for admin approval.');
    }
    
    redirect($returnTo);
}

