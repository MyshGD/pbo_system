function person_display_label(array $person): string
{
    $parts = [(string) ($person['full_name'] ?? '')];
    if (!empty($person['person_code'])) {
        $parts[] = (string) $person['person_code'];
    }
    if (!empty($person['department'])) {
        $parts[] = (string) $person['department'];
    }
    if (!empty($person['role_or_position'])) {
        $parts[] = (string) $person['role_or_position'];
    }

    return implode(' | ', array_filter($parts, static fn(string $part): bool => trim($part) !== ''));
}

