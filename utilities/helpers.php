function user_can(array $user, string $permission): bool
{
    $role = strtolower((string) ($user['role'] ?? ''));
    $permissions = [
        'admin' => ['*'],
        'staff' => [
            'view_dashboard',
            'view_operations',
            'record_operations',
            'view_reports',
        ],
    ];

    $allowed = $permissions[$role] ?? [];

    return in_array('*', $allowed, true) || in_array($permission, $allowed, true);
}

function require_permission(array $user, string $permission, string $fallback = 'dashboard.php'): void
{
    if (user_can($user, $permission)) {
        return;
    }

    set_flash('error', 'Your account does not have permission to access that function.');
    redirect($fallback);
}

function current_request_ip(): string
{
    $ip = (string) ($_SERVER['REMOTE_ADDR'] ?? 'unknown');

    return substr($ip !== '' ? $ip : 'unknown', 0, 45);
}

function current_user_id_from_session(): ?int
{
    return !empty($_SESSION['user_id']) ? (int) $_SESSION['user_id'] : null;
}

function encode_log_context(array|string|null $context): ?string
{
    if ($context === null) {
        return null;
    }

    if (is_string($context)) {
        return $context;
    }

    $encoded = json_encode($context, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE);

    return $encoded === false ? null : $encoded;
}

function audit_log(PDO $pdo, ?array $user, string $action, string $module, ?string $entityType = null, int|string|null $entityId = null, array|string|null $details = null): void
{
    try {
        $stmt = $pdo->prepare('INSERT INTO audit_logs (user_id, action, module, entity_type, entity_id, details, ip_address)
            VALUES (:user_id, :action, :module, :entity_type, :entity_id, :details, :ip_address)');
        $stmt->execute([
            'user_id' => isset($user['id']) ? (int) $user['id'] : current_user_id_from_session(),
            'action' => substr($action, 0, 80),
            'module' => substr($module, 0, 80),
            'entity_type' => $entityType !== null ? substr($entityType, 0, 80) : null,
            'entity_id' => $entityId !== null ? substr((string) $entityId, 0, 80) : null,
            'details' => encode_log_context($details),
            'ip_address' => current_request_ip(),
        ]);
    } catch (Throwable $e) {
        error_log('Audit log failed: ' . $e->getMessage());
    }
}

function log_system_issue(PDO $pdo, string $severity, string $message, array|string|null $context = null, ?array $user = null): void
{
    $validSeverities = ['notice', 'warning', 'error', 'critical'];
    if (!in_array($severity, $validSeverities, true)) {
        $severity = 'error';
    }

    try {
        $stmt = $pdo->prepare('INSERT INTO system_error_logs (user_id, severity, message, context, ip_address)
            VALUES (:user_id, :severity, :message, :context, :ip_address)');
        $stmt->execute([
            'user_id' => isset($user['id']) ? (int) $user['id'] : current_user_id_from_session(),
            'severity' => $severity,
            'message' => substr($message, 0, 255),
            'context' => encode_log_context($context),
            'ip_address' => current_request_ip(),
        ]);
    } catch (Throwable $e) {
        error_log('System issue log failed: ' . $e->getMessage());
    }
}

function configure_error_logging(PDO $pdo): void
{
    set_error_handler(static function (int $severity, string $message, string $file, int $line) use ($pdo): bool {
        if (!(error_reporting() & $severity)) {
            return false;
        }

        $level = in_array($severity, [E_USER_ERROR, E_RECOVERABLE_ERROR], true) ? 'error' : 'warning';
        log_system_issue($pdo, $level, $message, [
            'file' => $file,
            'line' => $line,
            'severity' => $severity,
        ]);

        return false;
    });

    register_shutdown_function(static function () use ($pdo): void {
        $error = error_get_last();
        if (!$error || !in_array((int) $error['type'], [E_ERROR, E_PARSE, E_CORE_ERROR, E_COMPILE_ERROR], true)) {
            return;
        }

        log_system_issue($pdo, 'critical', (string) $error['message'], [
            'file' => (string) $error['file'],
            'line' => (int) $error['line'],
            'type' => (int) $error['type'],
        ]);
    });
}

function page_param(string $name = 'page'): int
