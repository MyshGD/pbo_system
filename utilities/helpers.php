function inventory_stock_in(PDO $pdo, int $productId, int $quantity, float $unitCost, string $receivedDate, ?string $referenceNo, ?string $notes, ?array $user = null, string $sourceType = 'stock_in'): int
{
    if ($quantity <= 0) {
        return 0;
    }

    $productStmt = $pdo->prepare('SELECT id, type FROM products WHERE id = :id AND is_active = 1 FOR UPDATE');
    $productStmt->execute(['id' => $productId]);
    $product = $productStmt->fetch();
    if (!$product || $product['type'] !== 'item') {
        throw new RuntimeException('Stock can only be added to active inventory items.');
    }

    $batchCode = ($sourceType === 'opening_balance' ? 'OPEN-' : 'BATCH-') . $productId . '-' . date('YmdHis');
    $insertBatch = $pdo->prepare('INSERT INTO inventory_stock_batches (product_id, batch_code, received_date, quantity_received, quantity_remaining, unit_cost, source_type, reference_no, notes, created_by)
        VALUES (:product_id, :batch_code, :received_date, :quantity_received, :quantity_remaining, :unit_cost, :source_type, :reference_no, :notes, :created_by)');
    $insertBatch->execute([
        'product_id' => $productId,
        'batch_code' => $batchCode,
        'received_date' => $receivedDate,
        'quantity_received' => $quantity,
        'quantity_remaining' => $quantity,
        'unit_cost' => $unitCost,
        'source_type' => substr($sourceType, 0, 40),
        'reference_no' => $referenceNo !== '' ? $referenceNo : null,
        'notes' => $notes !== '' ? $notes : null,
        'created_by' => isset($user['id']) ? (int) $user['id'] : current_user_id_from_session(),
    ]);
    $batchId = (int) $pdo->lastInsertId();

    $insertMovement = $pdo->prepare('INSERT INTO inventory_stock_movements (product_id, batch_id, movement_date, movement_type, quantity_change, unit_cost, total_cost, reference_no, notes, created_by)
        VALUES (:product_id, :batch_id, :movement_date, "stock_in", :quantity_change, :unit_cost, :total_cost, :reference_no, :notes, :created_by)');
    $insertMovement->execute([
        'product_id' => $productId,
        'batch_id' => $batchId,
        'movement_date' => $receivedDate,
        'quantity_change' => $quantity,
        'unit_cost' => $unitCost,
        'total_cost' => $unitCost * $quantity,
        'reference_no' => $referenceNo !== '' ? $referenceNo : null,
        'notes' => $notes !== '' ? $notes : null,
        'created_by' => isset($user['id']) ? (int) $user['id'] : current_user_id_from_session(),
    ]);

    $updateProduct = $pdo->prepare('UPDATE products SET stock_qty = stock_qty + :quantity WHERE id = :id');
    $updateProduct->execute([
        'quantity' => $quantity,
        'id' => $productId,
    ]);

    return $batchId;
}

function inventory_fifo_issue(PDO $pdo, int $productId, int $quantity, string $movementDate, string $movementType, ?string $referenceNo, ?string $notes, ?array $user = null): array
{
    if ($quantity <= 0) {
        throw new RuntimeException('Quantity must be greater than zero.');
    }

    if (!in_array($movementType, ['sale', 'stock_out', 'adjustment'], true)) {
        throw new RuntimeException('Invalid stock movement type.');
    }

    $productStmt = $pdo->prepare('SELECT id, type, stock_qty FROM products WHERE id = :id AND is_active = 1 FOR UPDATE');
    $productStmt->execute(['id' => $productId]);
    $product = $productStmt->fetch();
    if (!$product || $product['type'] !== 'item') {
        throw new RuntimeException('FIFO stock can only be issued from active inventory items.');
    }

    if ((int) $product['stock_qty'] < $quantity) {
        throw new RuntimeException('Insufficient stock.');
    }

    $batchStmt = $pdo->prepare('SELECT id, quantity_remaining, unit_cost
        FROM inventory_stock_batches
        WHERE product_id = :product_id AND quantity_remaining > 0
        ORDER BY received_date ASC, id ASC
        FOR UPDATE');
    $batchStmt->execute(['product_id' => $productId]);
    $batches = $batchStmt->fetchAll();

    $remaining = $quantity;
    $totalCost = 0.0;
    $movementIds = [];
    $updateBatch = $pdo->prepare('UPDATE inventory_stock_batches SET quantity_remaining = quantity_remaining - :quantity WHERE id = :id AND quantity_remaining >= :required_quantity');
    $insertMovement = $pdo->prepare('INSERT INTO inventory_stock_movements (product_id, batch_id, movement_date, movement_type, quantity_change, unit_cost, total_cost, reference_no, notes, created_by)
        VALUES (:product_id, :batch_id, :movement_date, :movement_type, :quantity_change, :unit_cost, :total_cost, :reference_no, :notes, :created_by)');

    foreach ($batches as $batch) {
        if ($remaining <= 0) {
            break;
        }

        $available = (int) $batch['quantity_remaining'];
        $take = min($available, $remaining);
        if ($take <= 0) {
            continue;
        }

        $unitCost = (float) $batch['unit_cost'];
        $layerCost = $unitCost * $take;
        $updateBatch->execute([
            'quantity' => $take,
            'required_quantity' => $take,
            'id' => (int) $batch['id'],
        ]);
        if ($updateBatch->rowCount() === 0) {
            throw new RuntimeException('FIFO stock layer update failed.');
        }

        $insertMovement->execute([
            'product_id' => $productId,
            'batch_id' => (int) $batch['id'],
            'movement_date' => $movementDate,
            'movement_type' => $movementType,
            'quantity_change' => -$take,
            'unit_cost' => $unitCost,
            'total_cost' => $layerCost,
            'reference_no' => $referenceNo !== '' ? $referenceNo : null,
            'notes' => $notes !== '' ? $notes : null,
            'created_by' => isset($user['id']) ? (int) $user['id'] : current_user_id_from_session(),
        ]);
        $movementIds[] = (int) $pdo->lastInsertId();
        $totalCost += $layerCost;
        $remaining -= $take;
    }

    if ($remaining > 0) {
        throw new RuntimeException('Not enough FIFO stock layers.');
    }

    $updateProduct = $pdo->prepare('UPDATE products SET stock_qty = stock_qty - :quantity WHERE id = :id AND stock_qty >= :required_quantity');
    $updateProduct->execute([
        'quantity' => $quantity,
        'required_quantity' => $quantity,
        'id' => $productId,
    ]);
    if ($updateProduct->rowCount() === 0) {
        throw new RuntimeException('Product stock update failed.');
    }

    return [
        'total_cost' => $totalCost,
        'unit_cost' => $quantity > 0 ? $totalCost / $quantity : 0,
        'movement_ids' => $movementIds,
    ];
}

function date_filter_bounds(string $from, string $to): array
{
    $fromDate = DateTimeImmutable::createFromFormat('Y-m-d H:i:s', $from . ' 00:00:00') ?: new DateTimeImmutable('first day of this month 00:00:00');
    $toDate = DateTimeImmutable::createFromFormat('Y-m-d H:i:s', $to . ' 00:00:00') ?: new DateTimeImmutable('today 00:00:00');

    return [
        $fromDate->format('Y-m-d 00:00:00'),
        $toDate->modify('+1 day')->format('Y-m-d 00:00:00'),
    ];
}

