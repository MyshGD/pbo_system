function app_settings(?PDO $pdo = null, bool $refresh = false): array
{
    static $cache = null;

    if ($cache !== null && !$refresh) {
        return $cache;
    }

    $settings = system_setting_fallbacks();
    if (!$pdo instanceof PDO) {
        $cache = $settings;
        return $settings;
    }

    try {
        $stmt = $pdo->query('SELECT setting_key, setting_value FROM system_settings');
        foreach ($stmt->fetchAll() as $row) {
            $key = (string) ($row['setting_key'] ?? '');
            if ($key === '') {
                continue;
            }
            $settings[$key] = (string) ($row['setting_value'] ?? '');
        }
    } catch (Throwable $e) {
        error_log('System settings load failed: ' . $e->getMessage());
    }

    $cache = $settings;
    return $settings;
}

function app_setting(?PDO $pdo, string $key, ?string $fallback = null): string
{
    $settings = app_settings($pdo);
    $value = trim((string) ($settings[$key] ?? ''));

    if ($value !== '') {
        return $value;
    }

    $fallbacks = system_setting_fallbacks();
    return $fallback ?? (string) ($fallbacks[$key] ?? '');
}

function organization_profile(?PDO $pdo = null): array
{
    $university = app_setting($pdo, 'organization.university_name', 'Mindoro State University');
    $campus = app_setting($pdo, 'organization.campus_name', 'Bongabong Campus');
    $campusName = trim($university . ' ' . $campus);

    $logoPath = trim(app_setting($pdo, 'organization.logo_path', APP_LOGO));

    // Ensure logo path is web-root absolute so it works from routes like /pages/projects.php
    if ($logoPath === '') {
        $logoPath = APP_LOGO;
    }

    if (!preg_match('#^https?://#i', $logoPath)) {
        $basePath = app_base_path();

        if (strpos($logoPath, 'assets/') === 0) {
            $logoPath = $basePath . $logoPath;
        } elseif (strpos($logoPath, '/') !== 0) {
            $logoPath = $basePath . $logoPath;
        }
    }

    return [
        'university_name' => $university,
        'campus_name' => $campus,
        'office_name' => app_setting($pdo, 'organization.office_name', 'Production and Business Operation'),
        'system_name' => app_setting($pdo, 'organization.system_name', APP_SYSTEM_NAME),
        'logo_path' => $logoPath,
        'address' => app_setting($pdo, 'organization.address', ''),
        'contact_information' => app_setting($pdo, 'organization.contact_information', ''),
        'campus_display_name' => $campusName !== '' ? $campusName : APP_CAMPUS_NAME,
    ];
}

function save_system_settings(PDO $pdo, array $settings, array $user): void
{
    $stmt = $pdo->prepare('INSERT INTO system_settings (setting_key, setting_value, updated_by)
        VALUES (:setting_key, :setting_value, :updated_by)
        ON DUPLICATE KEY UPDATE setting_value = VALUES(setting_value), updated_by = VALUES(updated_by)');

    foreach ($settings as $key => $value) {
        $stmt->execute([
            'setting_key' => substr((string) $key, 0, 100),
            'setting_value' => (string) $value,
            'updated_by' => (int) $user['id'],
        ]);
    }

    app_settings($pdo, true);
}

