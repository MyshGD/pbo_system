function create_approval_request(
    PDO $pdo,
    int $requesterId,
    string $module,
    string $actionType,
    ?string $entityType = null,
    ?string $entityId = null,
    array|null $oldValue = null,
    array|null $newValue = null
): int {
    try {
        $stmt = $pdo->prepare('INSERT INTO approval_requests
            (requester_id, module, action_type, entity_type, entity_id, old_value, new_value, status, created_at, updated_at)
            VALUES (:requester_id, :module, :action_type, :entity_type, :entity_id, :old_value, :new_value, "pending", NOW(), NOW())');
        
        $stmt->execute([
            'requester_id' => $requesterId,
            'module' => substr($module, 0, 80),
            'action_type' => substr($actionType, 0, 80),
            'entity_type' => $entityType !== null ? substr($entityType, 0, 80) : null,
            'entity_id' => $entityId !== null ? substr((string) $entityId, 0, 80) : null,
            'old_value' => $oldValue !== null ? json_encode($oldValue, JSON_UNESCAPED_UNICODE) : null,
            'new_value' => $newValue !== null ? json_encode($newValue, JSON_UNESCAPED_UNICODE) : null,
        ]);

        $approvalId = (int) $pdo->lastInsertId();
        
        // Log the approval request creation
        audit_log($pdo, ['id' => $requesterId], 'approval_request_created', $module, $entityType, $entityId, [
            'action_type' => $actionType,
            'approval_id' => $approvalId,
        ]);

        return $approvalId;
    } catch (Throwable $e) {
        error_log('Approval request creation failed: ' . $e->getMessage());
        throw $e;
    }
}

function get_approval_request(PDO $pdo, int $approvalId): ?array
{
    $stmt = $pdo->prepare('SELECT ar.*, u.full_name AS requester_name, u.role AS requester_role,
        du.full_name AS decided_by_name
        FROM approval_requests ar
        LEFT JOIN users u ON u.id = ar.requester_id
        LEFT JOIN users du ON du.id = ar.decided_by_id
        WHERE ar.id = :id');
    
    $stmt->execute(['id' => $approvalId]);
    $result = $stmt->fetch();
    
    if ($result) {
        if ($result['old_value']) {
            $result['old_value'] = json_decode($result['old_value'], true);
        }
        if ($result['new_value']) {
            $result['new_value'] = json_decode($result['new_value'], true);
        }
    }
    
    return $result ?: null;
}

function list_approval_requests(PDO $pdo, ?string $status = 'pending', int $page = 1, int $limit = 50): array
{
    $offset = max(0, ($page - 1) * $limit);
    
    $query = 'SELECT ar.*, u.full_name AS requester_name, u.role AS requester_role,
        du.full_name AS decided_by_name
        FROM approval_requests ar
        LEFT JOIN users u ON u.id = ar.requester_id
        LEFT JOIN users du ON du.id = ar.decided_by_id';
    
    $params = [];
    
    if ($status !== null) {
        $query .= ' WHERE ar.status = :status';
        $params['status'] = $status;
    }
    
    $query .= ' ORDER BY ar.created_at DESC LIMIT :offset, :limit';
    
    $stmt = $pdo->prepare($query);
    foreach ($params as $key => $value) {
        $stmt->bindValue(':' . $key, $value);
    }
    $stmt->bindValue(':offset', $offset, PDO::PARAM_INT);
    $stmt->bindValue(':limit', $limit, PDO::PARAM_INT);
    $stmt->execute();
    
    $results = [];
    while ($row = $stmt->fetch()) {
        if ($row['old_value']) {
            $row['old_value'] = json_decode($row['old_value'], true);
        }
        if ($row['new_value']) {
            $row['new_value'] = json_decode($row['new_value'], true);
        }
        $results[] = $row;
    }
    
    return $results;
}

function approve_approval_request(PDO $pdo, int $approvalId, int $deciderId, ?string $remarks = null): bool
{
    try {
        $stmt = $pdo->prepare('UPDATE approval_requests
            SET status = "approved", admin_decision = "approved", admin_remarks = :remarks,
                decision_date = NOW(), decided_by_id = :decided_by_id, updated_at = NOW()
            WHERE id = :id AND status = "pending"');
        
        $stmt->execute([
            'id' => $approvalId,
            'decided_by_id' => $deciderId,
            'remarks' => $remarks,
        ]);
        
        if ($stmt->rowCount() > 0) {
            $request = get_approval_request($pdo, $approvalId);
            audit_log($pdo, ['id' => $deciderId], 'approval_request_approved', $request['module'], $request['entity_type'], $request['entity_id'], [
                'approval_id' => $approvalId,
                'action_type' => $request['action_type'],
            ]);
            return true;
        }
        return false;
    } catch (Throwable $e) {
        error_log('Approval request approval failed: ' . $e->getMessage());
        throw $e;
    }
}

