function pagination_meta(int $totalRows, int $page, int $perPage): array
{
    $totalPages = max(1, (int) ceil($totalRows / max(1, $perPage)));
    $page = min(max(1, $page), $totalPages);

    return [
        'page' => $page,
        'per_page' => $perPage,
        'total_rows' => $totalRows,
        'total_pages' => $totalPages,
        'offset' => ($page - 1) * $perPage,
    ];
}

function pagination_row_bounds(array $pagination): array
{
    $first = (int) $pagination['offset'] + 1;
    $last = (int) $pagination['offset'] + (int) $pagination['per_page'];

    return [$first, $last];
}

function pagination_url(int $page, string $param = 'page'): string
{
    $query = $_GET;
    $query[$param] = $page;
    $script = basename((string) ($_SERVER['PHP_SELF'] ?? 'index.php'));

    return $script . '?' . http_build_query($query);
}

function render_pagination(array $pagination, string $param = 'page'): void
{
    if ((int) $pagination['total_pages'] <= 1) {
        return;
    }

    $page = (int) $pagination['page'];
    $totalPages = (int) $pagination['total_pages'];
    $start = max(1, $page - 2);
    $end = min($totalPages, $page + 2);

    echo '<nav class="pagination" aria-label="Pagination">';
    echo '<a class="page-link' . ($page <= 1 ? ' disabled' : '') . '" href="' . h(pagination_url(max(1, $page - 1), $param)) . '">Previous</a>';
    for ($i = $start; $i <= $end; $i++) {
        echo '<a class="page-link' . ($i === $page ? ' active' : '') . '" href="' . h(pagination_url($i, $param)) . '">' . h((string) $i) . '</a>';
    }
    echo '<a class="page-link' . ($page >= $totalPages ? ' disabled' : '') . '" href="' . h(pagination_url(min($totalPages, $page + 1), $param)) . '">Next</a>';
    echo '</nav>';
}

